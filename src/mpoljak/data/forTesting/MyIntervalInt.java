package mpoljak.data.forTesting;

public class MyIntervalInt {
    private int min;
    private int max;
    public MyIntervalInt(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMax() {
        return max;
    }

    public int getMin() {
        return min;
    }
}
